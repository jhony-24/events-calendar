from dataclasses import dataclass

@dataclass
class User:
    def __init(self):
        self.id: int = ""
        self.username: str = ""
        self.email: str = ""
        self.password: str = ""
        self.fullName: str = ""
        self.avatar: str = ""
        self.description: str = ""
        self.createdDate: str = ""
